CREATE VIEW VW_CREDIT_CARD_CHURN AS (
    SELECT
        CIF AS ID, 
        CUSTOMERNAME AS CUSTOMER_NAME,
        CUSTSEX AS GENDER,
        AGE,
        CUSTMARITALSTATUS AS MARITAL_STATUS,
        CUSTLIMIT AS CREDIT_LIMIT, 
        TOTALACTIVECARD AS TOTAL_ACTIVE_CARD,
        CUSTVISACARD AS VISA_CARD_COUNT,
        CUSTMASTERCARDCARD AS MASTERCARD_COUNT,
        TO_DATE(LPAD(FIRSTDTEOPEN, 4, '0'),'YYMM') AS FIRST_DATE_OPEN,
        TO_DATE(LPAD(LASTDTEOPEN, 4, '0'), 'YYMM') AS LAST_DATE_OPEN,
        TO_DATE(LPAD(FIRSTEXPDATE, 4, '0'), 'YYMM') AS FIRST_EXP_DATE,
        TO_DATE(LPAD(LASTEXPDATE, 4, '0'), 'YYMM') AS LAST_EXP_DATE, 
        TOTALACTIVEMONTHS AS TOTAL_ACTIVE_MONTHS,
        ACTIVEYEARS AS ACTIVE_YEARS,
        TRX2022 AS TRANSACTIONS_2022,
        TRX2023 AS TRANSACTIONS_2023,
        TRX2024 AS TRANSACTIONS_2024, 
        TRX2025 AS TRANSACTIONS_2025,
        COUNTTRX AS TOTAL_TRANSACTIONS,
        AVGTRXPERMONTH AS AVG_TRX_PER_MONTH,
        ROUND(TOTALBILLINGAMOUNT,2) TOTAL_BILLING_AMOUNT,
        ROUND(AVGBILLINGAMOUNT, 2) AS AVG_BILLING_AMOUNT,
        CAST(LASTTRXDATE AS DATE) AS LAST_TRX_DATE,
        MONTHSSINCELASTTRX AS MONTHS_SINCE_LAST_TRX,
        USAGEPERCENT AS USAGE_PERCENT,
        CASE 
            WHEN DATEDIFF(DAY, CAST(LASTTRXDATE AS DATE), GETDATE()) > 365 THEN 1 ELSE 0
        END AS CHURN_TARGET
    FROM CREDIT_CARD_CHURN
) 